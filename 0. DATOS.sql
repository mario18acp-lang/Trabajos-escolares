-- Creación de la tabla Investigadores
CREATE TABLE Investigadores (
    investigador_id SERIAL PRIMARY KEY,
    nombre VARCHAR(50),
    apellido VARCHAR(50),
    especialidad VARCHAR(100),
    años_experiencia INT
);

-- Creación de la tabla Proyectos
CREATE TABLE Proyectos (
    proyecto_id SERIAL PRIMARY KEY,
    nombre_proyecto VARCHAR(100),
    fecha_inicio DATE,
    fecha_fin DATE,
    investigador_principal_id INT REFERENCES Investigadores(investigador_id)
);

-- Creación de la tabla Datos
CREATE TABLE Datos (
    dato_id SERIAL PRIMARY KEY,
    proyecto_id INT REFERENCES Proyectos(proyecto_id),
    variable1 FLOAT,
    variable2 FLOAT,
    categoría VARCHAR(50),
    fecha_registro DATE
);

